           column+=1
           sheet[row,column] = -πS[FQ_P[k]]*0.25*FQ[k]*(1-FQ_order_decision[k])-πS[FQ_P[k]]*0.25*0.25*(FQ[k]*FQ_order_decision[k]-FQ_Order_promised_quantites[k])
           column+=1
    end
end
