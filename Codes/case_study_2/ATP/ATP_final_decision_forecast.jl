    column=1
    row=1
    sheet[row,column] = "Order ID."
    column+=1
    sheet[row,column] = "Product type"
    column+=1
    sheet[row,column] = "Earliest due time"
    column+=1
    sheet[row,column] = "Latest due time"
    column+=1
    sheet[row,column] = "Netted quantites"
    column+=1
    sheet[row,column] = "Quantites"
    column+=1
    sheet[row,column] = "Rejection Penalty"
