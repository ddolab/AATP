    sheet[row,column] = "Promised quanitites"
    column+=1
    sheet[row,column] = "partial Penalty"
    column+=1
    sheet[row,column] = "denial Penalty"
    column+=1
    sheet[row,column] = "total Penalty"
    for k in eachindex(FQ_ID)
           row+=1
           column=1
           sheet[row,column] = FQ_ID[k]
           column+=1
           sheet[row,column] = FQ_P[k]
           column+=1
           sheet[row,column] = FQ_DE[k]
